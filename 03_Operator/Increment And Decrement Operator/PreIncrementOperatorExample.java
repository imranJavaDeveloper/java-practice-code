

public class PreIncrementOperatorExample {

    public static void main(String[] args) {

        int x=45;
        int x2=++x;

        ///  first increment than return updated value

        System.out.println(x);//46
        System.out.println(x2);//46


         char ch='A';
         char ch2=++ch;
        System.out.println(ch);//B
        System.out.println(ch2);//B

           double d=45024.54;
           double d2=++d;

        System.out.println(d);//45025.54
        System.out.println(d2);//45025.54
    }
}
