public class RelatioOperator {
    public static void main(String[] args) {
        
        int a=45,b=78;

        //Relational Operator
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        System.out.println(a==b);
        System.out.println(a!=b);
    }
}
