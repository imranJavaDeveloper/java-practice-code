
public class RelationOperator {
    public static void main(String[] args) {

        int x=45, b=25;

        System.out.println(x>b);//true
        System.out.println(x<b); // false
        System.out.println(x>=b);//ture
        System.out.println(x<=b);//false
        System.out.println(x==b); //false
        System.out.println(x!=b); //ture
    }
}
