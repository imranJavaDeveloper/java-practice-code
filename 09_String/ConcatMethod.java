
public class ConcatMethod {

    public static void main(String[] args) {

        // add two or more String

        String s1= new String("furkan");
        String s2= new String("imran");

        System.out.println(s1+" "+s2); // furkan imran

        System.out.println(s1.concat(s2)); // furkanimran
    }
}
